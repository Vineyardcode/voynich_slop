# 52e: CHARACTER-LEVEL COMPOSITIONALITY
# ============================================================
print("\n" + "=" * 65)
print("52e: CHARACTER-LEVEL COMPOSITIONALITY")
print("=" * 65)

# Instead of morphological decomposition, use pure character edges
# For words of length >= 4, take first-2 and last-2 characters
# Test: what fraction of observed (first2, last2) combinations exist?

min_word_len = 4
long_types = [w for w in word_types if len(w) >= min_word_len]
print(f"  Word types with length >= {min_word_len}: {len(long_types)}")

first2_set = set()
last2_set = set()
observed_combos = set()

for w in long_types:
    f2 = w[:2]
    l2 = w[-2:]
    first2_set.add(f2)
    last2_set.add(l2)
    observed_combos.add((f2, l2))

total_possible = len(first2_set) * len(last2_set)
fill_rate = len(observed_combos) / total_possible if total_possible > 0 else 0

print(f"  Unique first-2 patterns: {len(first2_set)}")
print(f"  Unique last-2 patterns:  {len(last2_set)}")
print(f"  Total possible combos:   {total_possible}")
print(f"  Observed combos:         {len(observed_combos)}")
print(f"  Fill rate:               {fill_rate:.4f} ({100*fill_rate:.2f}%)")

# Compare to random expectation: if independently distributed, what fill rate?
# For M types from N_first2 × N_last2 cells: expected fill = N*(1-(1-1/N)^M)
N_cells = total_possible
M_types = len(long_types)
expected_fill = N_cells * (1 - (1 - 1/N_cells)**M_types) / N_cells if N_cells > 0 else 0
print(f"  Random expected fill:    {expected_fill:.4f} ({100*expected_fill:.2f}%)")

# Permutation test
n_perm = 1000
perm_fills = []
f2_list = [w[:2] for w in long_types]
l2_list = [w[-2:] for w in long_types]
for _ in range(n_perm):
    shuffled_l2 = random.sample(l2_list, len(l2_list))
    perm_combos = set(zip(f2_list, shuffled_l2))
    perm_fills.append(len(perm_combos) / total_possible)

perm_mean = np.mean(perm_fills)
perm_std = np.std(perm_fills)
z_fill = (fill_rate - perm_mean) / perm_std if perm_std > 0 else 0

print(f"  Permutation fill rate:   {perm_mean:.4f} ± {perm_std:.4f}")
print(f"  z-score:                 {z_fill:.1f}")
print(f"  Direction: {'ANTI-compositional (fewer combos than random)' if z_fill < -2 else 'COMPOSITIONAL (more combos than random)' if z_fill > 2 else 'CONSISTENT WITH RANDOM'}")

# Also test with first-1 × last-1 (single character edges)
print(f"\n  Single-character edge test (first-1 × last-1):")
for wlen_min in [3, 4, 5]:
    sel = [w for w in word_types if len(w) >= wlen_min]
    f1s = set(w[0] for w in sel)
    l1s = set(w[-1] for w in sel)
    obs_c = set((w[0], w[-1]) for w in sel)
    total_c = len(f1s) * len(l1s)
    fill_c = len(obs_c) / total_c if total_c > 0 else 0
    
    # Permutation test
    f1_list = [w[0] for w in sel]
    l1_list = [w[-1] for w in sel]
    pf = []
    for _ in range(1000):
        sl = random.sample(l1_list, len(l1_list))
        pf.append(len(set(zip(f1_list, sl))) / total_c)
    pm = np.mean(pf)
    ps = np.std(pf)
    zc = (fill_c - pm) / ps if ps > 0 else 0
    
    print(f"    len>={wlen_min}: {len(f1s)}×{len(l1s)}={total_c} cells, "
          f"fill={100*fill_c:.1f}%, perm={100*pm:.1f}%, z={zc:.1f}")


